#!/usr/bin/env node
// Fetches the GradeX faculty CSV feed and writes src/data/gradexFaculty.js
// for the Faculty finder page.
//
// Source: https://gradex.bond/api/thedeck?action=faculty
// Usage: node scripts/fetch-gradex-faculty.js

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SOURCE_URL = 'https://gradex.bond/api/thedeck?action=faculty';
const OUTPUT_PATH = path.resolve(__dirname, '..', 'src', 'data', 'gradexFaculty.js');

const UA =
  'Mozilla/5.0 (compatible; AcademiaPortal/1.0; +https://github.com) Node/fetch';

/** RFC4180-style CSV parse (handles quoted fields and commas inside quotes). */
function parseCsv(text) {
  const raw = text.replace(/^\uFEFF/, '');
  const rows = [];
  let row = [];
  let field = '';
  let i = 0;
  let inQuotes = false;

  while (i < raw.length) {
    const c = raw[i];
    if (inQuotes) {
      if (c === '"') {
        if (raw[i + 1] === '"') {
          field += '"';
          i += 2;
          continue;
        }
        inQuotes = false;
        i += 1;
        continue;
      }
      field += c;
      i += 1;
      continue;
    }
    if (c === '"') {
      inQuotes = true;
      i += 1;
      continue;
    }
    if (c === ',') {
      row.push(field);
      field = '';
      i += 1;
      continue;
    }
    if (c === '\r') {
      i += 1;
      continue;
    }
    if (c === '\n') {
      row.push(field);
      if (row.some((cell) => String(cell).length > 0)) rows.push(row);
      row = [];
      field = '';
      i += 1;
      continue;
    }
    field += c;
    i += 1;
  }
  if (field.length || row.length) {
    row.push(field);
    if (row.some((cell) => String(cell).length > 0)) rows.push(row);
  }
  return rows;
}

function normalizeHeader(h) {
  return String(h || '')
    .trim()
    .toLowerCase()
    .replace(/\./g, '')
    .replace(/\s+/g, ' ');
}

const FIELD_MAP = {
  'sl no': 'serial',
  'faculty id': 'id',
  'faculty name': 'name',
  designation: 'designation',
  'staff room': 'room',
  department: 'department',
  email: 'email',
  mobile: 'mobile',
  'domain expertise': 'expertise',
};

async function main() {
  const res = await fetch(SOURCE_URL, {
    headers: { 'User-Agent': UA, Accept: 'text/csv,text/plain,*/*' },
  });
  if (!res.ok) throw new Error(`GET ${SOURCE_URL} → ${res.status}`);
  const text = await res.text();
  const table = parseCsv(text);
  if (table.length < 2) throw new Error('CSV has no data rows');

  const headerCells = table[0].map((h) => normalizeHeader(h));
  const colKeys = headerCells.map((h) => FIELD_MAP[h] || h.replace(/\s+/g, '_'));

  const staff = [];
  for (let r = 1; r < table.length; r++) {
    const cells = table[r];
    const obj = {};
    for (let c = 0; c < colKeys.length; c++) {
      const key = colKeys[c];
      if (!key) continue;
      obj[key] = String(cells[c] ?? '').trim();
    }
    if (!obj.id && !obj.name) continue;
    staff.push({
      id: obj.id || '',
      name: obj.name || '',
      designation: obj.designation || '',
      room: obj.room || '',
      department: obj.department || '',
      email: obj.email || '',
      mobile: obj.mobile || '',
      expertise: obj.expertise || '',
    });
  }

  const departments = [...new Set(staff.map((s) => s.department).filter(Boolean))].sort(
    (a, b) => a.localeCompare(b)
  );

  const payload = {
    source: SOURCE_URL,
    fetchedAt: new Date().toISOString(),
    departments,
    staff: staff.sort((a, b) => a.name.localeCompare(b.name)),
  };

  const preamble = `/* Auto-generated by scripts/fetch-gradex-faculty.js — run to refresh.
 * Feed: ${SOURCE_URL}
 * Third-party data (GradeX). Verify rooms and contacts with your department if needed.
 */

export default `;

  await fs.writeFile(OUTPUT_PATH, `${preamble}${JSON.stringify(payload, null, 2)};\n`, 'utf8');
  process.stderr.write(
    `Wrote ${staff.length} faculty, ${departments.length} departments → ${path.relative(process.cwd(), OUTPUT_PATH)}\n`
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
